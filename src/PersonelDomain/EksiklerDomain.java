package PersonelDomain;

public class EksiklerDomain {
	
	int id;
	private String yaka;
	private String merkez;
	private String unvan;
	private String adet;
	private String aciklama;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getYaka() {
		return yaka;
	}
	public void setYaka(String yaka) {
		this.yaka = yaka;
	}
	public String getMerkez() {
		return merkez;
	}
	public void setMerkez(String merkez) {
		this.merkez = merkez;
	}
	public String getUnvan() {
		return unvan;
	}
	public void setUnvan(String unvan) {
		this.unvan = unvan;
	}
	public String getAdet() {
		return adet;
	}
	public void setAdet(String adet) {
		this.adet = adet;
	}
	public String getAciklama() {
		return aciklama;
	}
	public void setAciklama(String aciklama) {
		this.aciklama = aciklama;
	}
	
	
	

}
